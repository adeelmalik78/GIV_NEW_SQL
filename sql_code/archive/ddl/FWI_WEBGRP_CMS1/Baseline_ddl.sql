(
  USER_ID       NUMBER NOT NULL,
  CONTENT_ID    NUMBER NOT NULL,
  CREATED_BY     VARCHAR2(20 BYTE)              DEFAULT USER                  NOT NULL,
  CREATED_DATE   DATE                           DEFAULT SYSDATE               NOT NULL,
  UPDATED_BY     VARCHAR2(20 BYTE)              DEFAULT USER                  NOT NULL,
